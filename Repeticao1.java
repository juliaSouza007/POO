class ImprimirNumeros {
    //Bloco main
	public static void main (String[] args) {
	    System.out.println("Imprimir numeros de 100 a 1000");
	    
        //imprimir os numeros
        for (int numero = 100; numero <= 1000; numero++) {
            System.out.println(numero);
        }
	}
}
